# ELIF = else if statement

nama = input("Nama kamu siapa? ")

if nama == "retno": # kondisi 1
    print(f"Hai {nama} ganteeeeng!") # aksi true 1
elif nama == "andi": # kondisi 2
    print(f"Hai {nama} kereeen!") # aksi true 2
elif nama == "dika": # kondisi 3
    print(f"Hai {nama} humoreeesh!") # aksi true 3
else:
    print(f"Maaf yaa nama {nama} belum terdaftar")
print("Ini adalah akhir dari program")