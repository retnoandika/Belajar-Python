# list -> array, mengakses dengan menggunakan index 
data_list = ['retno','andi','dika']

print(data_list)

# dictionary (dict) -> associative array
# identifier ->

data_dict = {
    'key':'value',
    'rt':'retno',
    'an':'andi',
    'dk':'dika',
    'nmr':100,
    'list':data_list
}

print(data_dict['rt'])
print(data_dict['dk'])
print(data_dict['nmr'])
print(data_dict['list'])